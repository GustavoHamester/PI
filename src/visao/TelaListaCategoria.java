    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tabCategorias.getSelectedRow();
        if (linha > -1) {
            Categoria categoria = categorias.get(linha);
            int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir a categoria " + categoria.getNome() + "?",
                    "confirme a exlus√£o", JOptionPane.YES_NO_OPTION);

            if (opcao == JOptionPane.YES_OPTION) {
                if (CategoriaDAO.excluir(categoria.getId())) {

                    JOptionPane.showMessageDialog(this, "Categoria excluida com sucesso!");
                    montarListaCategorias();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao exluir a categoria " + categoria.getNome() + ".");
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma categoria para excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

